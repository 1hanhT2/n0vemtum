SelectScrollDownButton.displayName =
  SelectPrimitive.ScrollDownButton.displayName

const SelectContent = React.forwardRef<
  React.ElementRef<typeof SelectPrimitive.Content>,
  React.ComponentPropsWithoutRef<typeof SelectPrimitive.Content>
>(({ className, children, position = "popper", ...props }, ref) => {
  const contentRef = React.useRef<HTMLDivElement | null>(null);

  const setRefs = React.useCallback(
    (node: HTMLDivElement | null) => {
      contentRef.current = node;
      if (typeof ref === "function") {
        ref(node);
      } else if (ref) {
        (ref as React.MutableRefObject<HTMLDivElement | null>).current = node;
      }

      if (node) {
        gsap.killTweensOf(node);
        gsap.fromTo(
          node,
          {
            opacity: 0,
            scale: 0.96,
            y: -4,
          },
          {
            opacity: 1,
            scale: 1,
            y: 0,
            duration: 0.22,
            ease: "power2.out",
          }
        );

        const items = node.querySelectorAll('[role="option"]');
        if (items.length > 0) {
          gsap.fromTo(
            items,
            { opacity: 0, x: -6 },
            {
              opacity: 1,
              x: 0,
              duration: 0.2,
              ease: "power2.out",
              stagger: 0.03,
              delay: 0.06,
            }
          );
        }
      }
    },
    [ref]
  );

  return (
    <SelectPrimitive.Portal>
      <SelectPrimitive.Content
        ref={setRefs}
        className={cn(
          "relative z-50 max-h-[--radix-select-content-available-height] min-w-[8rem] overflow-y-auto overflow-x-hidden rounded-md border border-border/60 text-popover-foreground shadow-lg backdrop-blur-xl bg-popover/80 dark:bg-popover/75 dark:border-border/40 origin-[--radix-select-content-transform-origin]",
          position === "popper" &&
            "data-[side=bottom]:translate-y-1 data-[side=left]:-translate-x-1 data-[side=right]:translate-x-1 data-[side=top]:-translate-y-1",
          className
        )}
        position={position}
        {...props}
      >
        <SelectScrollUpButton />
        <SelectPrimitive.Viewport
          className={cn(
            "p-1",
            position === "popper" &&
              "h-[var(--radix-select-trigger-height)] w-full min-w-[var(--radix-select-trigger-width)]"
          )}
        >
          {children}
        </SelectPrimitive.Viewport>
        <SelectScrollDownButton />
      </SelectPrimitive.Content>
    </SelectPrimitive.Portal>
  );
})
SelectContent.displayName = SelectPrimitive.Content.displayName

